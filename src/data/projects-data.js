export const getProjectById = (id) => {
    return projects.find(project => project.id === parseInt(id));
};

export const getRelatedProjects = (currentId, limit = 3) => {
    return projects.filter(project => project.id !== parseInt(currentId)).slice(0, limit);
};

export const getProjectsByCategory = (category) => {
    return projects.filter(project => project.category.toLowerCase() === category.toLowerCase());
};

export const getConsultationProjects = () => {
    return projects.filter(project => project.category === 'Consultation');
};

export const getConstructionProjects = () => {
    return projects.filter(project => project.category !== 'Consultation');
};

export const getProjectsByStatus = (status) => {
    return projects.filter(project => project.status.toLowerCase() === status.toLowerCase());
};